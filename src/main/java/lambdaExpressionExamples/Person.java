package lambdaExpressionExamples;

public class Person {
	private int id;
	private int age;

	public void setId(int id) {
		this.id = id;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public void print() {
		System.out.println("Hello");
	}

	public int getId() {
		return id;
	}

	public int getAge() {
		return age;
	}

}
